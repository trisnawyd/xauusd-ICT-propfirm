# STATS HELPERS
# ---------------------------------------------------------------------------
def stats(values):
    if not values:
        return {}
    pos = sum(1 for v in values if v > 0)
    return {
        'n':         len(values),
        'mean':      statistics.mean(values),
        'median':    statistics.median(values),
        'stdev':     statistics.stdev(values) if len(values) > 1 else 0,
        'win_rate':  pos / len(values) * 100,
        'min':       min(values),
        'max':       max(values),
    }

DAY_NAMES = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday']

def analyze(rows, label, period_note=''):
    by_dow = defaultdict(list)
    for r in rows:
        by_dow[r['dow']].append(r)

    out = []
    out.append(f"\n{'='*64}")
    out.append(f"  {label}")
    if period_note:
        out.append(f"  {period_note}")
    out.append(f"  Total bars: {len(rows)}  |  "
                f"{rows[0]['date'].strftime('%Y-%m-%d')} → {rows[-1]['date'].strftime('%Y-%m-%d')}")
    out.append(f"{'='*64}\n")
    out.append(f"{'Day':<12} {'N':>4}  {'AvgChg%':>8}  {'MedChg%':>8}  "
               f"{'WinRate':>8}  {'AvgPts':>8}  {'AvgRange':>9}  Bias")
    out.append('-' * 74)

    day_results = {}
    for i, day in enumerate(DAY_NAMES):
        rs = by_dow.get(i, [])
        if not rs:
            continue
        s = stats([r['chg_pct'] for r in rs])
        p = stats([r['chg_pts'] for r in rs])
        rng = statistics.mean([r['range'] for r in rs])
        bias = 'UP  ↑' if s['mean'] > 0 else 'DOWN ↓'
        out.append(
            f"{day:<12} {s['n']:>4}  {s['mean']:>+8.2f}%  {s['median']:>+8.2f}%  "
            f"{s['win_rate']:>7.1f}%  {p['mean']:>+8.1f}  {rng:>9.1f}  {bias}"
        )
        day_results[day] = s
    out.append('')
    return '\n'.join(out), day_results

