def week_breakdown(rows):
    from itertools import groupby
    # group by ISO week
    def iso_week(r):
        return r['date'].isocalendar()[:2]  # (year, week)

    rows_sorted = sorted(rows, key=lambda r: r['date'])
    out = []
    out.append(f"\n{'='*64}")
    out.append("  WEEK-BY-WEEK BREAKDOWN — Iran War Period")
    out.append(f"{'='*64}")
    out.append(f"{'Week':>10}  Mon    Tue    Wed    Thu    Fri   |  Net")
    out.append('-' * 64)

    for (yr, wk), group in groupby(rows_sorted, key=iso_week):
        week_rows = list(group)
        slots = {r['dow']: r for r in week_rows}
        cells = []
        net = 0.0
        for d in range(5):
            if d in slots:
                v = slots[d]['chg_pct']
                net += v
                cells.append(f"{v:>+5.1f}%")
            else:
                cells.append('  --- ')
        out.append(f" {yr}-W{wk:02d}  {'  '.join(cells)}  | {net:>+6.1f}%")

    out.append('')
    return '\n'.join(out)

