# PARSE
# ---------------------------------------------------------------------------
def parse(raw):
    rows = []
    prev_month = None
    year = 2024  # dataset begins Aug 2024 (~$2519)

    for line in raw.strip().split('\n'):
        parts = line.split(',')
        mm, dd = map(int, parts[0].split(' ')[0].split('/'))

        if prev_month is not None and mm == 1 and prev_month == 12:
            year += 1

        date = datetime(year, mm, dd)
        o, h, l, c = float(parts[1]), float(parts[2]), float(parts[3]), float(parts[4])

        rows.append({
            'date': date,
            'dow': date.weekday(),          # 0=Mon … 4=Fri
            'day': date.strftime('%A'),
            'open': o, 'high': h, 'low': l, 'close': c,
            'chg_pts': c - o,               # intraday point change
            'chg_pct': (c - o) / o * 100,
            'range':   h - l,
        })
        prev_month = mm
    return rows

