# PATTERN CONFIRMATION
# ---------------------------------------------------------------------------
def confirm_pattern(day_results, hypothesis):
    """
    hypothesis: dict { day_name: 'up'|'down' }
    Returns match count and details.
    """
    out = []
    out.append("\n--- Pattern Confirmation ---")
    matches = 0
    for day, expected in hypothesis.items():
        if day not in day_results:
            continue
        actual_dir = 'up' if day_results[day]['mean'] > 0 else 'down'
        win = day_results[day]['win_rate']
        icon = '✓' if actual_dir == expected else '✗'
        out.append(
            f"  {icon} {day:<12}  expected={expected.upper():<4}  "
            f"actual={'UP' if actual_dir=='up' else 'DOWN'}  "
            f"win_rate={win:.1f}%"
        )
        if actual_dir == expected:
            matches += 1
    out.append(f"\n  Pattern match: {matches}/{len(hypothesis)} days confirmed")
    return '\n'.join(out)

